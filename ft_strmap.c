#include "libft.h"

char	*ft_strmap(char const *s, char (*f) (char))
{
	size_t	i;
	size_t	len;
	char	*str;

	if (s && f)
	{
		len = ft_strlen(s);
		str =  ft_memalloc(len);
		i = 0;
		while (i < len && s[i])
		{
			str[i] = f(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
